public class Char01 {
	public static void main(String[] args) {
		char n1 = 86;
		System.out.println(n1); // V
		System.out.println((int)n1); // 86
		char n2 = 'V';
		System.out.println(n2);

		char n3 = 'уе';
		System.out.println(n3);
		System.out.println((int)n3);
		char n4 = 24353;
		System.out.println(n4);
		System.out.println((int)n4);
	}
}