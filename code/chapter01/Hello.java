public class Hello {
	public static void main(String[] args) {
		System.out.println("张晨林 is studying java!");
	}
}

// 1. public class Hello 表示 Hello 是一个类, 是一个public共有的类
// 2. Hello{ } 表示一个类的开始和结束
// 3. public static void main(String[] args) 表示一个主方法，即我们程序的入口
// 4. main() {} 表示方法的开始和结束
// 5. System.out.println("hello,world~"); 表示输出"hello,world~"到屏幕
// 6. ; 表示语句结束